# Задача №1: Условные операторы
def main():
    # Запрашиваем ввод трех чисел
    a = float(input("Введите первое число: "))
    b = float(input("Введите второе число: "))
    c = float(input("Введите третье число: "))

    # Определяем наибольшее число
    max_num = max(a, b, c)
    print(f"Наибольшее число: {max_num}")

    # Определяем наименьшее число
    min_num = min(a, b, c)
    print(f"Наименьшее число: {min_num}")

    # Проверяем равенство всех чисел
    if a == b == c:
        print("Все числа равны.")
    else:
        print("Числа не равны.")


if __name__ == "__main__":
    main()